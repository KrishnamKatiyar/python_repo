# Python lists are containers to store a set of values of any data type.
# A list can be indexed just like a string.
# Unlike strings, Lists are mutable.

friends = ["Apple", "Banana", "Mango", 3, 6.774, "Ravi", "Potato"]

print(friends)

print(friends[2])

friends[2] = "Grapes"
print(friends[2])

print(friends)
print(friends[0:5])