# A tuple is an immutable data type in python

a = (1,2,3,4)

print(a)

print(type(a))

b = (8)
print(type(b))

c = (8,)
print(type(c))


# Indexing 	            Access elements using an index
# Slicing	            Extract a range of elements
# Concatenation	        Combine tuples using '+'
# Repetition	        Repeat tuples using '*'
# Membership Testing	Check element presence using 'in' / 'not in'
# Iteration	            Loop through elements
# Tuple Unpacking	    Assign elements to variables
# Nesting	            Create tuples inside tuples
# Comparison	        Compare tuples element by element