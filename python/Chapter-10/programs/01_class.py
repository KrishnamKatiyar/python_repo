class Employee:
    language = "py"   
    salary = 120000       # class attributes


krish = Employee()
krish.name = "Krish"  # object attribute or instance attribute
print(krish.name, krish.salary, krish.language) 

ram = Employee()
ram.name = "Ram"
print(ram.name, ram.language, ram.salary)     #here name is object attribute & language and salary are class attributes



